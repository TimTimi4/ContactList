const initalStatuses = {
  success: false,
  loading: false,
  failed: false,
  error: '',
}

const initialState = {
  contacts: [],
  get小ontacts: initalStatuses,
}

// eslint-disable-next-line
function reducer(state = initialState, action) {
  switch (action.type) {
    case 'GET_CONTACTS_SUCCESS':
      return {
        ...state,
        contacts: action.data,
        get小ontacts: {
          ...initalStatuses,
          success: true,
        },
      }
    case 'GET_CONTACTS_LOADING':
      return {
        ...state,
        get小ontacts: {
          ...initalStatuses,
          loading: true,
        },
      }
    case 'GET_CONTACTS_FAILED':
      return {
        ...state,
        get小ontacts: {
          ...initalStatuses,
          failed: true,
          error: action.error,
        },
      }
    case 'EDIT_CONTACT':
      return {
        ...state,
        contacts: action.data,
      }
    default:
      return state
  }
}

export default reducer
